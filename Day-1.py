# Basic python code for a band-name generator. 
# Setting the welcome message.
print("Welcome to the Band Name Generator.")
# Asks the user for the city that they grew up in.
city_name = input("What city did you grow up in?\n")
# Asks the user for the name of a pet.
pet_name = input("What is the name of your pet?\n")
# Combines the name of their city and pet and show them a potential band name.
print("The name of your band could be " + city_name + " " + pet_name)
